uint8_t count = 0;

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance==htim4.Instance)
	{
		HAL_GPIO_TogglePin(LED_B_GPIO_Port, LED_B_Pin);
		CvtCurSteps(FB, stepperA.steps, stepperB.steps, stepperC.steps, (uint8_t *)buffF);
		CDC_Transmit_FS((uint8_t *)buffF, 20);
	}
	else if (htim->Instance==htim9.Instance && buzzer == true)
	{
		count++;
		if(count == 1)
		{
			HAL_GPIO_WritePin(BUZZER_GPIO_Port, BUZZER_Pin, GPIO_PIN_SET); // turn on buzzer
		}
		else
		{
			HAL_GPIO_WritePin(BUZZER_GPIO_Port, BUZZER_Pin, GPIO_PIN_RESET); // turn off buzzer
			if(count == 15)
				count = 0;
		}
	}
}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==htim5.Instance)
	{
		(stepperA.steps > 0) ? stepperA.steps = stepperA.steps-1: HAL_TIM_PWM_Stop(&htim5, TIM_CHANNEL_3);
	}
	else if(htim->Instance==htim2.Instance)
	{
		(stepperB.steps > 0) ? stepperB.steps = stepperB.steps-1: HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1);
	}
	else if(htim->Instance==htim3.Instance)
	{
		(stepperC.steps > 0) ? stepperC.steps = stepperC.steps-1: HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_3);
	}
	if(((stepperA.steps == 0) && (stepperB.steps == 0) && (stepperC.steps == 0)))
	{				
		if(calib == true)
		{
			HAL_TIM_PWM_Stop(&htim5, TIM_CHANNEL_3);
			HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1);
			HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_3);
			switch (state)
			{
				case 2:
					state = 3;
					CDC_Transmit_FS((uint8_t *)C3, 2);
					break;
				case 4:
					state = 5;
					CDC_Transmit_FS((uint8_t *)C5, 2);
					break;
				case 6:
					state = 1;
					calib = false;
					CDC_Transmit_FS((uint8_t *)C7, 2);
					break;	
			}
		}
		else
		{
			HAL_TIM_PWM_Stop(&htim5, TIM_CHANNEL_3);
			HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1);
			HAL_TIM_PWM_Stop(&htim3, TIM_CHANNEL_3);
			HAL_TIM_Base_Stop(&htim4);
			HAL_GPIO_TogglePin(LED_G_GPIO_Port, LED_G_Pin);
			CvtCurSteps(FB, stepperA.steps, stepperB.steps, stepperC.steps, (uint8_t *)buffF);
			CDC_Transmit_FS((uint8_t *)buffF, 20);
		}					
	}
}

void CvtCurSteps(char X, uint32_t A, uint32_t B, uint32_t CC, uint8_t* Outp)
{
	Outp[0] = X;
	Outp[1] = A/100000 + 48;
	Outp[2] = A%100000/10000 + 48;
	Outp[3] = A%100000%10000/1000 + 48;
	Outp[4] = A%100000%10000%1000/100 + 48;
	Outp[5] = A%100000%10000%1000%100/10 + 48;
	Outp[6] = A%10 + 48;
	Outp[7] = B/100000 + 48;
	Outp[8] = B%100000/10000 + 48;
	Outp[9] = B%100000%10000/1000 + 48;
	Outp[10] = B%100000%10000%1000/100 + 48;
	Outp[11] = B%100000%10000%1000%100/10 + 48;
	Outp[12] = B%10 + 48;
	Outp[13] = CC/100000 + 48;
	Outp[14] = CC%100000/10000 + 48;
	Outp[15] = CC%100000%10000/1000 + 48;
	Outp[16] = CC%100000%10000%1000/100 + 48;
	Outp[17] = CC%100000%10000%1000%100/10 + 48;
	Outp[18] = CC%10 + 48;
	Outp[19] = '\n';
}

